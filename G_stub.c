__attribute__((hot))
inline void aecc_sub (uint64_t *r, const uint64_t *a, const uint64_t *b) {
  
  uint64_t a0, a1, a2, a3, b0, b1, b2, b3, p;

  a0 = a[0];
  a1 = a[1];
  a2 = a[2];
  a3 = a[3];

  b0 = b[0];
  b1 = b[1];
  b2 = b[2];
  b3 = b[3];
  
  size_t borrow = 0;

  asm("subq %5, %1\n\t"
      "sbbq %6, %2\n\t"
      "sbbq %7, %3\n\t"
      "sbbq %8, %4\n\t"
      "sbbq $0, %0"
      : "+r" (borrow), "+r" (a0),  "+r" (a1), "+r" (a2), "+r" (a3)
      : "r" (b0), "r" (b1), "r" (b2), "r" (b3)
      : "cc");
  
  if(borrow == 0){
    r[0] = a0;
    r[1] = a1;
    r[2] = a2;
    r[3] = a3;
    return;
  }
  
  p = 0x1000003d1;

  asm("subq %4, %0\n\t"
      "sbbq $0, %1\n\t"
      "sbbq $0, %2\n\t"
      "sbbq $0, %3"
      : "+r" (a0),  "+r" (a1), "+r" (a2), "+r" (a3)
      : "r" (p)
      : "cc");

  r[0] = a0;
  r[1] = a1;
  r[2] = a2;
  r[3] = a3;
    
  return;
}

