  const uint64_t ap1 = ap[1];
  const uint64_t bp1 = bp[1];
  const uint64_t ap0 = ap[0];
  const uint64_t bp0 = bp[0];
  if (ap3 != bp3)
    return ap3 > bp3 ? 1 : -1;
  if (ap2 != bp2)
    return ap2 > bp2 ? 1 : -1;
